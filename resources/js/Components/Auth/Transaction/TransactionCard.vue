function swipeHandler(direction, event) {
  var elem = event.target;
  var listelems = document.querySelectorAll('.transaction-list__card');
  if (direction == 'left') {
    if (elem.classList.contains('swipeRight')) {
      elem.classList.remove('swipeRight');
    } else {
      listelems.forEach(element => {
        element.classList.remove('swipeRight');
        element.classList.remove('swipeLeft');
      });
      elem.classList.add('swipeLeft');
    }
  } else if (direction == 'right') {
    if (elem.classList.contains('swipeLeft')) {
      elem.classList.remove('swipeLeft');
    } else {
      listelems.forEach(element => {
        element.classList.remove('swipeRight');
        element.classList.remove('swipeLeft');
      });
      elem.classList.add('swipeRight');
    }
  }
}

